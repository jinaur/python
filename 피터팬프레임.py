a = input()
l = [['..#..'], ['.#.#.'], ['#.'+a[0]+'.#'], ['.#.#.'], ['..#..']]

for i in range(1, len(a)) :
    s = '#'
    if (i+1)%3 == 0 :
        s = '*'
    if (i+2)%3 == 0 and len(a) >= i+2 :
        l[0][0] += '.#..'
        l[1][0] += '#.#.'
        l[2][0] += '.'+a[i]+'.'
        l[3][0] += '#.#.'
        l[4][0] += '.#..'
    else :
        l[0][0] += '.'+s+'..'
        l[1][0] += s+'.'+s+'.'
        if s == '*' :    
            l[2][0] += s+'.'+a[i]+'.'+s
        else :
            l[2][0] += '.'+a[i]+'.'+s
        l[3][0] += s+'.'+s+'.'
        l[4][0] += '.'+s+'..'

for i in range(0, 5) :
    print(l[i][0])

